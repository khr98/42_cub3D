#include "libft.h"

char		*ft_strtrim(char const *s1, char const *set)
{
	size_t	len;
	char	*ret;

	if (!s1 || !set)
		return (0);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	len = ft_strlen(s1);
	while (len && ft_strchr(set, *(s1 + len)))
		len--;
	len++;
	if (!(ret = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	ft_strlcpy(ret, s1, len + 1);
	return (ret);
}
